public class ProblemCount extends BaseBean {

    private String settleNum;
    private String noSettleNum;
    private String number;

    public String getSettleNum() {
        return settleNum;
    }

    public void setSettleNum(String settleNum) {
        this.settleNum = settleNum;
    }

    public String getNoSettleNum() {
        return noSettleNum;
    }

    public void setNoSettleNum(String noSettleNum) {
        this.noSettleNum = noSettleNum;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
